import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

console.log('Generating KaTeX constants...');

// 读取 KaTeX 文件
const katexJSPath = path.join(__dirname, '..', 'node_modules', 'katex', 'dist', 'katex.min.js');
const katexCSSPath = path.join(__dirname, '..', 'node_modules', 'katex', 'dist', 'katex.min.css');

let katexJS = '';
let katexCSS = '';

try {
	katexJS = fs.readFileSync(katexJSPath, 'utf-8');
	console.log('Read KaTeX JS, length:', katexJS.length);
} catch (error) {
	console.error('Error reading KaTeX JS:', error);
	process.exit(1);
}

try {
	katexCSS = fs.readFileSync(katexCSSPath, 'utf-8');
	// 移除字体引用，使用系统字体
	katexCSS = katexCSS.replace(/@font-face[^}]+}/g, '');
	console.log('Read KaTeX CSS, length:', katexCSS.length);
} catch (error) {
	console.error('Error reading KaTeX CSS:', error);
	process.exit(1);
}

// 生成TypeScript常量文件 - 使用Base64编码避免转义问题
const katexJSBase64 = Buffer.from(katexJS, 'utf-8').toString('base64');
const katexCSSBase64 = Buffer.from(katexCSS, 'utf-8').toString('base64');

const output = `// Auto-generated file - do not edit
// Generated by scripts/generate-katex-constants.js

export const KATEX_JS_BASE64 = "${katexJSBase64}";

export const KATEX_CSS_BASE64 = "${katexCSSBase64}";
`;

// 写入文件
const outputPath = path.join(__dirname, '..', 'katex-constants.ts');
fs.writeFileSync(outputPath, output, 'utf-8');

console.log('Generated katex-constants.ts');
console.log('Output file:', outputPath);
